        if std is not None:
            std_val_int, _ = parse_std_to_int(std)
            if std_val_int is not None:
                pct = lookup_percentile(std_val_int)
                if pct is not None: pct_txt = str(pct)

        ae_txt = lookup_age_equivalent("alici" if sub=="alici" else "ifade", res["raw"]) or "—"

