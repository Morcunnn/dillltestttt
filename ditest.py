    def _switch_to(self, which:str, jump:bool):
        self.current_subtest = which
        items = self.items[which]
        self.filtered_ids = [it["id"] for it in items]

        alt = "ALICI DİL" if which=="alici" else "İFADE EDİCİ DİL"
        other = "İFADE EDİCİYE GEÇ" if which=="alici" else "ALICIYA DÖN"
        self.header.config(text=f"Alt Test: {alt} | Yaş: {self.age_year}y {self.age_month}a {self.age_day}g | Çocuk: {self.child_name}")
        self.switch_btn.config(text=other)

        self.finish_btn.pack_forget()
        if which=="alici":
            self.finish_btn.pack(anchor="w", pady=12)

        start_id = self.age_start[which].get(self.age_year, items[0]["id"]) if 2 <= self.age_year <= 7 else items[0]["id"]
        
        self.current_id = start_id if jump else items[0]["id"]

        self._refresh_table()
        self._select_id(self.current_id, True)

    def _refresh_table(self):
        items = self.items[self.current_subtest]
        resp = self.responses[self.current_subtest]
        self.tree.delete(*self.tree.get_children())
        for it in items:
            if it["id"] not in self.filtered_ids: continue
            v = resp[it["id"]]
            self.tree.insert("", "end", iid=str(it["id"]),
                             values=(it["id"], it["age"], ("" if v is None else str(v)), it["text"]))

    def _on_select(self, _=None):
        sel = self.tree.selection()
        if not sel: return
        self.current_id = int(sel[0])
        self._show_detail(self.current_id)

    def _show_detail(self, iid:int):
        items = self.items[self.current_subtest]
        resp = self.responses[self.current_subtest]
        it = next(x for x in items if x["id"] == iid)
        self.det_title.config(text=f"{it['id']}. {it['age']}")
        self.det_txt.config(state="normal")
        self.det_txt.delete("1.0", "end")
        self.det_txt.insert("1.0", it["text"])
        self.det_txt.config(state="disabled")
        self.resp_var.set("" if resp[iid] is None else str(resp[iid]))

    def apply_search(self):
        q = (self.search_var.get() or "").lower().strip()
        items = self.items[self.current_subtest]
        if not q: self.filtered_ids = [it["id"] for it in items]
        else: self.filtered_ids = [it["id"] for it in items if q in f"{it['id']} {it['age']} {it['text']}".lower()]
        self._refresh_table()

    def _set_response(self):
        v = self.resp_var.get()
        resp = self.responses[self.current_subtest]
        if v == "": resp[self.current_id] = None
        elif v in ("0","1"): resp[self.current_id] = int(v)
        self._refresh_table()

    def _quick_set(self, v:int):
        self.responses[self.current_subtest][self.current_id] = v
        self.resp_var.set(str(v))
        self._refresh_table()

    def _select_id(self, iid:int, center:bool=False):
        self.tree.selection_set(str(iid))
        if center: self.tree.see(str(iid))
        self._show_detail(iid)

    def go_first(self):
        if self.filtered_ids: self._select_id(self.filtered_ids[0], True)

    def go_last(self):
        if self.filtered_ids: self._select_id(self.filtered_ids[-1], True)

    def go_prev(self):
        try:
            current_idx = self.filtered_ids.index(self.current_id)
            if current_idx > 0: self._select_id(self.filtered_ids[current_idx-1], True)
        except ValueError: pass

    def go_next(self):
        try:
            current_idx = self.filtered_ids.index(self.current_id)
            if current_idx < len(self.filtered_ids)-1: self._select_id(self.filtered_ids[current_idx+1], True)
        except ValueError: pass

    def go_jump(self):
        try:
            iid = int(self.jump_var.get())
            ids = [it["id"] for it in self.items[self.current_subtest]]
            if iid in ids: self._select_id(iid, True)
            else: messagebox.showwarning("Uyarı", "Geçersiz ID.")
        except: messagebox.showwarning("Uyarı", "Bir sayı girin.")

    def _calc_and_show(self, sub:str):
        items = self.items[sub]
        resp = self.responses[sub]
        
        start_id = self.age_start[sub].get(self.age_year, items[0]["id"]) if 2 <= self.age_year <= 7 else items[0]["id"]
