def age_bin_key(y:int, m:int) -> str:
    for sy,sm,ey,em in AGE_BINS:
        if (y>sy or (y==sy and m>=sm)) and (y<ey or (y==ey and m<=em)):
            return f"{sy}-{sm}_{ey}-{em}"
    return "UNK"


def compute_score(items:List[Dict[str,Any]], responses:Dict[int,Optional[int]], age_start_id:int) -> Dict[str,Any]:
    ids = [it["id"] for it in items]
    idx = {iid:i for i,iid in enumerate(ids)}
    vals = [0 if responses[iid] is None else int(responses[iid]) for iid in ids]
    start_index = idx.get(age_start_id, 0)

    ceiling_index = None
    for i in range(start_index, len(vals)-2):
        if vals[i]==vals[i+1]==vals[i+2]==0:
            ceiling_index = i
            break

    basal_candidates = []
    end_check = (ceiling_index if ceiling_index is not None else len(vals))
    for i in range(start_index, end_check-2):
        if vals[i]==vals[i+1]==vals[i+2]==1:
            basal_candidates.append(i)
    basal_index = basal_candidates[-1] if basal_candidates else None

    boosted_ranges = []
    if basal_index is not None:
        boosted_ranges.append((ids[0], ids[basal_index-1]))
        for i in range(0, basal_index):
            vals[i] = 1
    
    if ceiling_index is not None:
        for j in range(ceiling_index + 3, len(vals)):
            vals[j] = 0

    raw = sum(vals)
    wrong = [ids[i] for i,v in enumerate(vals) if v==0]
    
    return {
        "raw": raw,
        "basal_start_id": (ids[basal_index] if basal_index is not None else None),
        "ceiling_start_id": (ids[ceiling_index] if ceiling_index is not None else None),
        "boosted_before_ranges": boosted_ranges,
        "wrong_item_ids": wrong,
        "values_applied": {ids[i]: vals[i] for i in range(len(ids))}
    }

# ========= Norm tabloları / Yardımcı Fonksiyonlar =========
NORM_TABLES: Dict[str,Any] = {}


def load_norms_from_data(data: Dict[str, Any]):
    global NORM_TABLES
    NORM_TABLES.update(data)


def load_norms_from_json(path:str):
    global NORM_TABLES
    with open(path, "r", encoding="utf-8") as f:
        data = json.load(f)
    if not isinstance(data, dict):
        raise ValueError("JSON sözlük olmalı.")
    NORM_TABLES.update(data)


def lookup_standard_score(subtest:str, raw:int, y:int, m:int) -> Optional[str]:
    tbl = NORM_TABLES.get(subtest)
    if not tbl:
        return None
    col = age_bin_key(y, m)
    # Ham puanın üst sınırı kontrol ediliyor
    if str(raw) not in tbl.get("rows", {}):
        return None
    row = tbl.get("rows", {}).get(str(raw))
    if not row:
        return None
    return row.get(col)


def parse_std_to_int(val:str) -> Tuple[Optional[int], str]:
    if val is None:
        return (None, "")
    s = str(val).strip()
    if s.startswith("<"):
        try:
            # Sınır değerini döndür (örn: <50 -> 50)
            return (int(s[1:]), "capped_low")
        except:
            return (None, "")
    if s.startswith(">"):
        try:
            # Sınır değerini döndür (örn: >164 -> 164)
            return (int(s[1:]), "capped_high")
        except:
            return (None, "")
    try:
        return (int(s), "")
    except:
        return (None, "")


def lookup_composite(total_std:int) -> Optional[str]:
    comp = NORM_TABLES.get("composite")
    if not comp:
        return None
    for row in comp:
        mn_sum = row.get("min_sum")
        mx_sum = row.get("max_sum")
        if (mn_sum is None or total_std>=mn_sum) and (mx_sum is None or total_std<=mx_sum):
            return str(row.get("std"))
    return None


def lookup_percentile(std_score_int:int) -> Optional[int]:
    tbl = NORM_TABLES.get("percentiles")
    if not tbl:
        return None
    
    for row in tbl:
        # Tek standart puan eşleşmesi
        if "std" in row and row.get("std") == std_score_int:
            return row.get("pct")

        # Aralık eşleşmesi (capped değerler için)
        min_std = row.get("min_std")
        max_std = row.get("max_std")
        if min_std is not None and max_std is not None and min_std <= std_score_int <= max_std:
            return row.get("pct")
            
    return None


def lookup_age_equivalent(subtest:str, raw:int) -> Optional[str]:
    ae = NORM_TABLES.get("age_equiv", {}).get(subtest)
    if not ae:
        return None
    for row in ae:
        mn = row.get("min")
        mx = row.get("max")
        label = row.get("age")
        if (mn is None or raw>=mn) and (mx is None or raw<=mx):
            return str(label)
    return None

