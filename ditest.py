    def save_session(self):
        payload = {"test":"TEDİL - Alıcı+İfade", "child_name": self.child_name, "age_year": self.age_year, "age_month": self.age_month, "age_day": self.age_day, "timestamp": ts(), "current_subtest": self.current_subtest, "responses": {"alici": self.responses["alici"], "ifade": self.responses["ifade"]}}
        f = filedialog.asksaveasfilename(title="Oturumu Kaydet", defaultextension=".json", initialfile=f"tedil_session_{payload['timestamp']}.json", filetypes=[("JSON","*.json")])
        if not f: return
        with open(f, "w", encoding="utf-8") as fh:
            json.dump(payload, fh, ensure_ascii=False, indent=2)
        messagebox.showinfo("Kaydedildi", f"Oturum kaydedildi:\n{f}")

    def load_session(self):
        f = filedialog.askopenfilename(title="Oturum Yükle", filetypes=[("JSON","*.json")])
        if not f: return
        try:
            with open(f, "r", encoding="utf-8") as fh: data = json.load(fh)
            if data.get("test") not in ("TEDİL - Alıcı+İfade", "TEDİL - Alıcı Dil"): raise ValueError("Uygun dosya değil.")
            self.child_name = data.get("child_name", "Anonim")
            self.age_year = data.get("age_year")
            self.age_month = data.get("age_month")
            self.age_day = data.get("age_day")
            r = data.get("responses", {})
            if isinstance(r, dict):
                alici_r, ifade_r = r.get("alici", {}), r.get("ifade", {})
                for k, v in alici_r.items():
                    try: iid = int(k); self.responses["alici"][iid] = v if v is None else int(v)
                    except ValueError: pass
                for k, v in ifade_r.items():
                    try: iid = int(k); self.responses["ifade"][iid] = v if v is None else int(v)
                    except ValueError: pass
            self._switch_to(data.get("current_subtest", "alici"), False)
            messagebox.showinfo("Yüklendi", "Oturum yüklendi.")
        except Exception as e: messagebox.showerror("Hata", f"Yüklenemedi:\n{e}")

